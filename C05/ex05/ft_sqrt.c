int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb == 1)
		return (1);
	if (nb >= 100000000)
	{
		while (i <= (nb / 10000))
		{
			if ((i * i) == nb)
				return (i);
			i++;
		}
	}
	else
	{
		while (i <= (nb / 2))
		{
			if ((i * i) == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
